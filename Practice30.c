#include <stdio.h>
int main()
{
    printf("\n");
    printf("============================\n");
    printf("Printing Numbers from 1 to 5\n");
    printf("============================\n");
    int i = 1;
    while (i <= 5)
    {
        printf("\n%d", i);
        i++;
    }
    printf("\n\n");
    printf("============================\n");
    return 0;
}